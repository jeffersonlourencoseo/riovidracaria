export function getBairrosPorRegiao() {
  const grouped: Record<string, Bairro[]> = {};
  for (const bairro of bairros) {
    if (!grouped[bairro.regiao]) grouped[bairro.regiao] = [];
    grouped[bairro.regiao].push(bairro);
  }
  return grouped;
}

export function getBairrosPorSubregiao(regiao: string) {
  const grouped: Record<string, Bairro[]> = {};
  for (const bairro of bairros) {
    if (bairro.regiao !== regiao) continue;
    const key = bairro.subregiao || 'Outros';
    if (!grouped[key]) grouped[key] = [];
    grouped[key].push(bairro);
  }
  return grouped;
}
